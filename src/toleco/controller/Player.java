package toleco.controller;

/**
* An enum representing all possible players that can be playing the game. A
* player may either be kPlayer1 or kPlayer2.
*
* @author Evan Ralston (Javadocs)
* @author Adam Armstrong (Implementation)
* @version 1.0
*/
public enum Player
{
    /**
    * An enumerated constant for the first player.
    */
    kPlayer1,
    
    /**
    * An enumerated constant for the second player.
    */
    kPlayer2;
}

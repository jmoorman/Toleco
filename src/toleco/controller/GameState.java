package toleco.controller;

/**
* An enum representing all the possible states a battle (game) can be in.
*
* @author Eriq Augusinte (Javadocs)
* @author Adam Armstrong (Implementation)
* @version 1.0
*/
public enum GameState
{
    /**
    * Represents the game state where a player has a terrain (possibly containing
    * a unit) selected.
    */
    kTerrainSelected,
    
    /**
    * Represents the game state where a player has nothing selected.
    */
    kNoneSelected,
    
    /**
    * Represents the game state where a player has a unit selected and has indicated
    * a desire to attack.
    */
    kAttackMode,
    
    /**
    * Represents the game state where the battle is over. Either a player won or
    * the game was quit.
    */
    kGameOver
}

package toleco.unit;

/**
* Represents the various different types of armor that a Unit may have. All
* Units have an ArmorType.
*
* @author Andrew Barton (Javadocs)
* @author Adam Armstrong (Implementation)
* @version 1.0
*/
public enum ArmorType
{
    /**
    * This represents armor made of hide.
    */
    kHide,
    /**
    * This represents armor made of one or many bones.
    */
    kBone,
    /**
    * This represents armor made out of a squishy material.
    */
    kPadded;
}
